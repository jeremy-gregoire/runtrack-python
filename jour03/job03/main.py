def Add(a: int, b: int) -> None:
  """
  Affiche dans la console, l'addition entre deux nombres.

  #### Parameters
   - a: int > Une expression numérique correspondant au premier nombre à additionner.
   - b: int > Une expression numérique correspondant au deuxième nombre à additionner.
  """
  print(a + b)


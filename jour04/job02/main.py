def affiche_deuxieme_elements() -> None:
  """
  Cette fonction affiche le deuxième élément d'une liste prédéfinie de fruits.
  """
  # Liste de fruits
  fruits = ["pomme", "cerise", "orange"]

  # Affiche le deuxième élément de la liste
  print(fruits[1])

# Appel de la fonction
affiche_deuxieme_elements()

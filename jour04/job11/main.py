# Ma liste de nombre
L = [7, 11, 42, 39, 2]

# Affiche la liste de nombre actuel
print("La liste actuel:", L)

# Pour chaque nombre de la liste de nombre
for i in range(len(L)):
  # Additionne se nombre avec 1
  L[i] += 1

# Affiche la liste de nombre après opération
print("La liste après modification:", L)